// Close any modal currently shown
function closeCurrentModal() {
  var shown = document.querySelector('.modal.show');
  if (shown) {
    shown.classList.remove('show');
    shown.style.display = 'none';
  }
}

// Enhance close buttons for accessibility and keyboard controls
document.addEventListener('DOMContentLoaded', function() {
  document.querySelectorAll('.close').forEach(function(el) {
    // Ensure aria role + keyboard focus
    if (!el.hasAttribute('role')) el.setAttribute('role', 'button');
    if (!el.hasAttribute('tabindex')) el.setAttribute('tabindex', '0');
    if(!el.hasAttribute('aria-label')) el.setAttribute('aria-label','Close');
    el.addEventListener('keydown', function(e) {
      if (e.key === 'Enter' || e.key === ' ') {
        e.preventDefault();
        el.click();
      }
    });
  });

  // Close modal when clicking on overlay area (outside modal-content)
  document.querySelectorAll('.modal').forEach(function(modal) {
    modal.addEventListener('click', function(e) {
      if (e.target === modal) {
        // Hide clicked overlay
        modal.classList.remove('show');
        modal.style.display = 'none';
      }
    });
  });

  // Allow ESC key to close currently open modal
  document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') {
      closeCurrentModal();
    }
  });
});